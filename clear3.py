# ===================== 3. 定义岗位→类别的映射（批量分类） =====================
def get_job_category(job_name):
    """根据岗位名称，自动匹配对应的类别"""
    job_name = str(job_name).strip()
    if job_name in ["Java", "C/C++"]:
        return "后端开发类"
    elif job_name == "前端开发":
        return "前端开发类"
    elif job_name in ["软件测试", "测试工程师"]:
        return "软件测试类"
    elif job_name == "硬件测试":
        return "硬件测试类"
    elif job_name == "技术支持工程师":
        return "技术支持类"
    elif job_name == "实施工程师":
        return "实施交付类"
    elif job_name in ["项目经理/主管", "项目专员/助理", "项目招投标"]:
        return "项目管理类"
    elif job_name in ["质量管理/测试", "质检员"]:
        return "质量管理类"
    elif job_name == "产品专员/助理":
        return "产品相关类"
    elif job_name == "科研人员":
        return "科研类"
    else:
        # 默认归类（可根据实际岗位补充）
        return "后端开发类"

# ===================== 4. 批量给5000+岗位生成画像 =====================
# 给每个岗位匹配类别
df["岗位类别"] = df[job_column].apply(get_job_category)

# 批量赋值画像字段（将模板中的字段批量添加到DataFrame中）
for field in ["专业技能", "证书要求", "创新能力", "学习能力", "抗压能力", "沟通能力", "实习能力"]:
    df[field] = df["岗位类别"].apply(lambda x: job_template[x][field])

